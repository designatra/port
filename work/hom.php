                <p class="clmn_2">Beauty.marked was her first identity that I created. I wanted to focus on the Marked portion. I took an uppercase R and adjusted it so that it would some what look like a lowercase m. I kept the leg of the uppercase R and took away its stem.I used Gestalt’s law of closure by adding a dot under the leg of the R to complete the lowercase m. The dot also is representation of the “beauty mark”. The logo also represents the artist, Micaela Jordan, by having her initials [MRJ] in the design. The m is created by the uppercase R and the dot, the R is the main portion of the logo, and the lowercase j’s dot is what is used to create the “beauty mark.” I then took the logo and used it for the HOM logo which looks like a house because it houses all of artistries. </p></div>
